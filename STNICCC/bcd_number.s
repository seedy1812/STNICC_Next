frame_reset:
bcd_reset:
   ld hl,bcd_number_start
   ld a,00
   ld b, bcd_number_end - bcd_number_start
bcd_reset_loop:
   ld (hl),a
   inc hl
   djnz bcd_reset_loop
   ret


frame_draw:
   ret
   ld de , $6000
   ld a, 0
   call  reset_sprite_index
   ld a, 8
   call bcd_draw
 ;  call DBG_CheckFirstFrame
   ret


DBG_CheckFirstFrame:
   ld hl,0
   ld (DBG_count),hl

   ld hl,bcd_number_start
   ld de,bcd_number_compare

// stop on fram 15 bcd

   ld bc,4
.cmploop:
   ld a,(de)
   inc de
   cpi
   ret nz
   jp pe,.cmploop
   my_break
   ld hl, DBG_frame_hit
   set 0,(hl)
   ret



DBG_Break:
   push de
   push hl

   ld hl,DBG_frame_hit
   bit 0,(hl)
   jr z,.no

   ld hl,(DBG_count)
   inc hl
   ld (DBG_count),hl

   ccf
   ld de,(BDG_count_index)
   sbc hl,de
   jr c, .no


;   my_break
 .no:
   pop hl
   pop de
   ret

DBG_frame_hit: db 0
DBG_count:     dw 0
BDG_count_index:  dw 0 ;$70

bcd_number_start:   db 0,0,0,0

bcd_number_end: equ *

bcd_number_compare :  db 0,0,$00,$01

frame_inc:
   ld a,1

bcd_add_byte:
    ld e,0
    and a
    ld hl , bcd_number_end-1
    add a,(hl)
 bcd_add_loop:
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,e
    adc a,(hl)  
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,e
    adc a,(hl)
    daa
    ld (hl),a
    ret nc
    dec hl
    ld a,e
    adc a,(hl)
    daa
    ld (hl),a
    ret



draw_irq_counter:
   ld de, $20+32
   ld a,$20+32
   ld hl, irq_last_count
   ld b,1
   jr bcd_outer_loop

memory_draw:
   ld de, $80+32
   ld a,16+32
   ld hl, mem_page
   ld b ,1
   call bcd_outer_loop


   ld de, $A0+32
   ld a,16+32
   ld hl, memory_ptr+1
   ld b,1
   call bcd_outer_loop

   ld de, $b0+32
   ld a,16+32
   ld hl, memory_ptr
   ld b,1
   jr bcd_outer_loop

bcd_draw:
   ld a,8
   ld hl, bcd_number_start
   ld b,4
   jr bcd_outer_loop

bcd_outer_loop:
   ld (bcd_y),a
bcd_mid_loop:
   push bc
   ld a,$f0
   and (hl)
   swapnib 

   call print_bcd_number
   add de,8

   ld a,$f
   and (hl)
   inc hl
   call print_bcd_number
   add de,8
   pop bc
   djnz bcd_mid_loop
   ret

bcd_load_sprites:
   ld hl,numbers
   ld bc, $303b

   xor a
   out (c),a ; start at pattern 0

   ld bc,$005b
   ld a,16 ; 16 - 0 ->f sprites

.bcd_load_loop:
   otir ;; send 256 bytes to port 0x5b
   dec a
   jr nz, .bcd_load_loop

   call palette_create_sprites

   nextreg $43,%00100000 ; palette sprite 2 normal
;   nextreg $40,0         ; start at colour 0 and auto increment
;   nextreg $44,0
;   nextreg $44,0
;   nextreg $44,255
;   nextreg $44,1

   nextreg $4b,0

   ld bc , $303B
   ld a,0
   out (c),a

   ret

reset_sprite_index
   ld bc , $303B
 ;brea  sub a
   out (c),a
   ret

; de = posx 9bit
; a = index
print_bcd_number

  ld b,a
   ld c,$57

   out (c),e  ; x:lo
 bcd_y: equ *+1
   ld a,9
   out (c),a ; y

   ld a,1      ; palette offset = 0
   and d       ; no mirror and no rotate
   or $e0      ; 4 bit palette offset
   out (c),a   ; bit 0 msb:x
   
   set 7,b     
   out (c),b   ; visible+ sprite_value
 
   ret

